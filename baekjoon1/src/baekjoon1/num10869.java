package baekjoon1;

import java.util.Scanner;

public class num10869 {

	public static void main(String[] args) {
		// 사칙연산
	    Scanner sc = new Scanner(System.in);
	    
	    int A = sc.nextInt();
	    int B = sc.nextInt();
	    
	    System.out.println(A+B);
	    System.out.println(A-B);
	    System.out.println(A*B);
	    System.out.println(A/B);
	    System.out.println(A%B);    
	        
	    sc.close();		
		
	}

}
